/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */

  buttonInit(); // 버튼 구조체 변수들 초기화

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	        // 1. 모든 버튼 상태 실시간 스캔 (Non-blocking 디바운스)
	        buttonScan();

	        // 2. 외부 1층 버튼 체크 -> LED_EXT_1F (PA12) 토글
	        if (buttonWasPressed(BTN_EXT_1F_UP))
	        {
	            ledToggle(LED_EXT_1F);
	        }

	        // 3. 외부 2층 버튼 체크 (UP 또는 DOWN) -> LED_EXT_2F (PA9) 토글
	        if (buttonWasPressed(BTN_EXT_2F_UP) || buttonWasPressed(BTN_EXT_2F_DOWN))
	        {
	            ledToggle(LED_EXT_2F);
	        }

	        // 4. 외부 3층 버튼 체크 (UP 또는 DOWN) -> LED_EXT_3F (PB2) 토글 [수정 완료]
	        if (buttonWasPressed(BTN_EXT_3F_UP) || buttonWasPressed(BTN_EXT_3F_DOWN))
	        {
	            ledToggle(LED_EXT_3F);
	        }

	        // 5. 외부 4층 버튼 체크 (DOWN) -> LED_EXT_4F (PA8) 토글 [수정 완료]
	        if (buttonWasPressed(BTN_EXT_4F_DOWN))
	        {
	            ledToggle(LED_EXT_4F);
	        }

	        // 6. 내부 버튼 체크 (어떤 내부 버튼이든 누르는 순간) -> LED_INT (PA11) 토글
	        if (buttonWasPressed(BTN_INT_1F)   ||
	            buttonWasPressed(BTN_INT_2F)   ||
	            buttonWasPressed(BTN_INT_3F)   ||
	            buttonWasPressed(BTN_INT_4F)   ||
	            buttonWasPressed(BTN_INT_OPEN) ||
	            buttonWasPressed(BTN_INT_CLOSE))
	        {
	            ledToggle(LED_INT);
	        }

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

